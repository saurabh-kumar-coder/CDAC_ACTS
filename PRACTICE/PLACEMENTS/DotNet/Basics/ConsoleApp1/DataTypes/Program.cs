namespace DataTypes
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int a = 20;
            Console.WriteLine(a);

            char ch = 'a';
            Console.WriteLine(ch);
            Console.WriteLine((int)ch);

            float f = 20.02f;
            Console.WriteLine(f);
            Console.WriteLine((int)f);
        }
    }
}
